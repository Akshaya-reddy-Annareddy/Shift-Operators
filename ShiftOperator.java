public class ShiftOperator {
    public static void main(String[] args) {
        int a = 16;
        int b = 4;
        
        System.out.println("Left Shift Operator (<<):");
        System.out.println("a << 1: " + (a << 1));
        System.out.println("b << 2: " + (b << 2));

        System.out.println("\nRight Shift Operator (>>):");
        System.out.println("a >> 1: " + (a >> 1));
        System.out.println("b >> 2: " + (b >> 2));
    }
}



